public class Calculator {
    static int sum(int a, int b){

        return a+b;
    }
    static int sub(int a, int b){

        return a-b;
    }
    static int times(int a, int b){

        return a*b;
    }
    static int divide(int a, int b){

        return a/b;
    }
    static int secret(int a, int b){

        return (a+1*b)*(b+125*2+a);
    }


}
